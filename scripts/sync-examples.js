// Regenerates web/examples.js from examples/*.js, so the web editor's
// inlined copies (used so it works standalone, no fetch/server needed)
// never drift from the CLI examples.
//
// Existing entries keep their name/label and ordering as long as the
// matching file in examples/ still exists; new files are appended
// (alphabetically) with a label derived from the filename.

var fs = require('fs');
var path = require('path');
var vm = require('vm');

var EXAMPLES_DIR = path.join(__dirname, '..', 'examples');
var OUTPUT_FILE = path.join(__dirname, '..', 'web', 'examples.js');

function loadExistingEntries() {
  if (!fs.existsSync(OUTPUT_FILE)) {
    return [];
  }

  var sandbox = { window: {} };
  var code = fs.readFileSync(OUTPUT_FILE, 'utf8');

  vm.createContext(sandbox);
  vm.runInContext(code, sandbox);

  return sandbox.window.TEXTMODE_EXAMPLES || [];
}

function encodeCode(source) {
  var lines = source.split('\n');

  // A trailing newline in the file produces one empty trailing element;
  // drop it since each kept line already carries its own '\n'.
  if (lines.length > 0 && lines[lines.length - 1] === '') {
    lines.pop();
  }

  return lines.map(function (line) {
    return JSON.stringify(line + '\n');
  }).join(' +\n');
}

function deriveLabel(name) {
  return name.split(/[-_]+/).map(function (word) {
    return word.charAt(0).toUpperCase() + word.slice(1);
  }).join(' ');
}

function main() {
  var existing = loadExistingEntries();
  var existingByName = {};
  existing.forEach(function (entry) {
    existingByName[entry.name] = entry;
  });

  var namesOnDisk = {};
  var files = fs.readdirSync(EXAMPLES_DIR).filter(function (f) {
    return f.endsWith('.js');
  });
  files.forEach(function (f) {
    namesOnDisk[path.basename(f, '.js')] = true;
  });

  var entries = [];

  existing.forEach(function (entry) {
    if (namesOnDisk[entry.name]) {
      entries.push({ name: entry.name, label: entry.label });
    } else {
      console.log('Removed (no longer in examples/): ' + entry.name);
    }
  });

  files.map(function (f) {
    return path.basename(f, '.js');
  }).filter(function (name) {
    return !existingByName[name];
  }).sort().forEach(function (name) {
    var label = deriveLabel(name);
    console.log('Added: ' + name + ' (label "' + label + '" - edit web/examples.js by hand and re-run to keep a better one)');
    entries.push({ name: name, label: label });
  });

  var blocks = entries.map(function (entry) {
    var source = fs.readFileSync(path.join(EXAMPLES_DIR, entry.name + '.js'), 'utf8');

    return '  {\n' +
      '    name: ' + JSON.stringify(entry.name) + ',\n' +
      '    label: ' + JSON.stringify(entry.label) + ',\n' +
      '    code:\n' + encodeCode(source) + '\n' +
      '  }';
  });

  var header =
    '// Inlined copies of ../examples/*.js, so the web editor works standalone\n' +
    '// (no fetch, no server needed) even when opened straight from disk.\n' +
    '// Generated by scripts/sync-examples.js - do not edit by hand, edit the\n' +
    '// files in examples/ instead and re-run: node scripts/sync-examples.js\n' +
    '\n' +
    'window.TEXTMODE_EXAMPLES = [\n';

  var footer = '\n];\n';

  fs.writeFileSync(OUTPUT_FILE, header + blocks.join(',\n') + footer);

  console.log('Wrote ' + entries.length + ' example(s) to ' + path.relative(process.cwd(), OUTPUT_FILE));
}

main();
